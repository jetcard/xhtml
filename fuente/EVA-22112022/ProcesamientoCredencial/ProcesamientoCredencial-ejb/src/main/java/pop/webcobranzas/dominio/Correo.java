public class Correo {

    private int corId;
    private String corTipo;
    private String corDesc;

    public int getCorId() {
        return corId;
    }

    public void setCorId(int corId) {
        this.corId = corId;
    }

    public String getCorTipo() {
        return corTipo;
    }

    public void setCorTipo(String corTipo) {
        this.corTipo = corTipo;
    }

    public String getCorDesc() {
        return corDesc;
    }

    public void setCorDesc(String corDesc) {
        this.corDesc = corDesc;
    }
    
    

}
